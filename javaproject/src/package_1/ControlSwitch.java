package package_1;

public class ControlSwitch
{

	public static void main(String[] args)
	{
		String color="green";
		{
			switch("color")
			{
			case "red":
			{
				System.out.println("red");
				break;
			}
			case"green":
			{
				System.out.println("green");
				break;
			}
			case "blue":
			{
				System.out.println("blue");
				break;
			}
			default:
			{
				 System.out.println("invalid");
			}
			}
		}
	}
}
			
			
			
			
			
			
			
			
			
			
			
